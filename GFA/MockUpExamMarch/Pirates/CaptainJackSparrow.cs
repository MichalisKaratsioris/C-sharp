using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Pirates
{
    public class CaptainJackSparrow : Pirate // Inheritance
    {

//----------- Constructor -----------
        public CaptainJackSparrow(string name) : base(name)
        {
        }
// Overriding the methods that separate Captain Jack Sparrow from a normal Pirate
        public override void work()
        {
            this.goldAmount += 4;
            this.healthPoints -= 3;
        }

        public override void party()
        {
            this.healthPoints += 5;
        }
    }
}




















































































































































































































































































































































